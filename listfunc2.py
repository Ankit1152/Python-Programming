# index()
# count()
# max()
# min()
# sort()
# reverse()


l = [30 , 20 , 60 , 25 , 10 , 10]
s = ["Hello" , "World"]
print(l.index(10))
print(s.index("World"))

print(l.count(10))

print(max(l))
print(max(s))     # W > H

print(min(l))
print(min(s))     # H < W

l.sort()          # Ascending Order
print(l)
s.sort()
print(s)

l.reverse()
print(l)
s.reverse()
print(s)






